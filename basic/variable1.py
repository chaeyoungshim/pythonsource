# 변수 - 타입이 없음(자바스크립트랑 비슷, 할당하면 타입이 생김)
# str, int, float, bool

# 숫자형 - 정수, 실수, 8진수, 16진수..
a = 13 # 타입이 없어서 그냥 선언만 하면 됨
a = -7
a = 0
a = 1.2
a = 0o177 # 8진수
a = 0x8fff # 16진수


# 사칙연산
# a = 3
# b = 4
a, b = 3, 4 # 앞쪽으로 맞춰서 한꺼번에 처리하기
print(a + b) # 7
print(a / b) # 0.75
print(a // b) # 0 (정수 나눗셈)
print(a % b) # 3
print(a ** b) # 81 (제곱)
