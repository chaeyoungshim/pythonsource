# 이스케이프 문자
# tab(\t), \로 시작하는 문자들

print("안녕하세요\t파이썬 연습 중입니다.")      # tab 
print("안녕하세요\nt파이썬 연습 중입니다.")     # enter
print("안녕하세요\\t파이썬 연습 중입니다.")     # \ 하나 넣어주기
print()
print("Python's facorite food is perl")        # 바깥을 쌍따옴표로 가면 안에는 홑따옴표 마음대로 쓸 수 있음
print('"Python"s facorite food is perl')       #
print('Python\'s facorite food is perl')       # \를 주고 따옴표를 주면 따옴표를 출력하겠다는 것을 명시, 단지 기호라고 알려주는 것
print("\"Python\"s facorite food is perl")     # 여기도 따옴표라는 것을 알려주기
